    private void btnBuscarProduto1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProduto1ActionPerformed
        try {
            if (!txtIdProduto.getText().equals("")) {
                produtoA = produtoDAO.buscarPorId(Integer.parseInt(txtIdProduto.getText()));
                if (produtoA == null) {
                    JOptionPane.showMessageDialog(null, "Produto não encontrado!");
                } else {
                    txtDescricaoProduto.setText(produtoA.toString());
                }
                txtPreco.setText(String.valueOf(produtoA.getPreco()));
            } else {
                JOptionPane.showMessageDialog(null, "Digite um id de produto");
            }
        } catch (DAOException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar produto");
        }
    }//GEN-LAST:event_btnBuscarProduto1ActionPerformed

