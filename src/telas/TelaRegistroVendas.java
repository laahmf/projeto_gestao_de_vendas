    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        ArrayList<ItemVenda> itens = new ArrayList<>();
        String dataVenda = txtDataDaVenda.getText();
        
        if (produtoA != null && !txtQuant.getText().equals("")){
            ItemVenda item = new ItemVenda(produtoA, Integer.parseInt(txtQuant.getText()));
            itens.add(item);
        }
        if (produtoB != null && !txtQuant2.getText().equals("")){
            ItemVenda item = new ItemVenda(produtoB, Integer.parseInt(txtQuant2.getText()));
            itens.add(item);
        }
        if (produtoC != null && !txtQuant3.getText().equals("")){
            ItemVenda item = new ItemVenda(produtoC, Integer.parseInt(txtQuant3.getText()));
            itens.add(item);
        }
        if (produtoD != null && !txtQuant4.getText().equals("")){
            ItemVenda item = new ItemVenda(produtoD, Integer.parseInt(txtQuant4.getText()));
            itens.add(item);
        }
        if (produtoE != null && !txtQuant5.getText().equals("")){
            ItemVenda item = new ItemVenda(produtoE, Integer.parseInt(txtQuant5.getText()));
            itens.add(item);
        }
        
        if (txtDataDaVenda.getValue() != null && !itens.isEmpty() && cliente != null && precoTotal > 0) {
            try {
                Venda venda = new Venda(dataVenda, cliente, itens, precoTotal);
                vendaDAO.registrarVenda(venda);
                btnLimparActionPerformed(evt);
                JOptionPane.showMessageDialog(null, "Venda registrada com sucesso!");
            } catch (SQLException | DAOException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao registrar venda!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos e adicione pelo menos um produto!");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

