    private void btnBuscarPrecoTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPrecoTotalActionPerformed
        precoTotal = 0;

        if (produtoA != null && !txtQuant.getText().equals(""))
            precoTotal = precoTotal + (produtoA.getPreco() * Integer.parseInt(txtQuant.getText()));
        if (produtoB != null && !txtQuant2.getText().equals(""))
            precoTotal = precoTotal + (produtoB.getPreco() * Integer.parseInt(txtQuant2.getText()));
        if (produtoC != null && !txtQuant3.getText().equals(""))
            precoTotal = precoTotal + (produtoC.getPreco() * Integer.parseInt(txtQuant3.getText()));
        if (produtoD != null && !txtQuant4.getText().equals(""))
            precoTotal = precoTotal + (produtoC.getPreco() * Integer.parseInt(txtQuant4.getText()));
        if (produtoE != null && !txtQuant5.getText().equals(""))
            precoTotal = precoTotal + (produtoC.getPreco() * Integer.parseInt(txtQuant5.getText()));

        txtPrecoTotal.setText(String.valueOf(precoTotal));
    }//GEN-LAST:event_btnBuscarPrecoTotalActionPerformed

