    private void populateTableFornecedores() {
        try {
            ((DefaultTableModel) tblFornecedores.getModel()).setRowCount(0);
            ArrayList<Fornecedor> fornecedores = fornecedorDAO.buscar();
            for (Fornecedor fornecedor : fornecedores) {
                Object[] objs = {fornecedor.getIdFornecedor(), fornecedor.getNome(),
                    fornecedor.getTelefone(), fornecedor.getEmail(),
                    fornecedor.getCnpj()};
                ((DefaultTableModel) tblFornecedores.getModel()).addRow(objs);
            }
        } catch (DAOException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na conex√£o com o Banco de dados!");
            ex.printStackTrace();
        }
    }

