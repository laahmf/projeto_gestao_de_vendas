    private void populateTableProdutos() {
        try {
            ((DefaultTableModel) tblProdutos.getModel()).setRowCount(0);
            ArrayList<Produto> produtos = produtoDAO.buscar();
            for (Produto produto : produtos) {
                Object[] objs = {produto.getIdProduto(), produto.getNome(), produto.getQuantidade(),
                    produto.getPreco(), produto.getCategoria(), produto.getFornecedor()};
                ((DefaultTableModel) tblProdutos.getModel()).addRow(objs);
            }
        } catch (DAOException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na conex√£o com o Banco de dados!");
            ex.printStackTrace();
        }
    }

