    private void populateTableClientes() {
        try {
            ((DefaultTableModel) tblClientes.getModel()).setRowCount(0);
            ArrayList<Cliente> clientes = clienteDAO.buscar();
            for (Cliente cliente : clientes) {
                Object[] objs = {cliente.getIdCliente(), cliente.getNome(),
                    cliente.getTelefone(), cliente.getEmail(), cliente.getRg(),
                    cliente.getCpf(), cliente.getDataNascimento()};
                ((DefaultTableModel) tblClientes.getModel()).addRow(objs);
            }
        } catch (DAOException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na conex√£o com o Banco de dados!");
            ex.printStackTrace();
        }
    }

