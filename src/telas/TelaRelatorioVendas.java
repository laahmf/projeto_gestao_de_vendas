    private void populateTblRelatorioVendas() {
        try {
            ((DefaultTableModel) tblRelatorioVendas.getModel()).setRowCount(0);
            ArrayList<Venda> vendas  = vendaDAO.buscar();
            for (Venda venda : vendas) {
                Object[] objs = {venda.getIdVenda(), venda.getCliente().getNome(),
                    venda.getDataVenda(), venda.getPrecoTotal()
                };
                ((DefaultTableModel) tblRelatorioVendas.getModel()).addRow(objs);
            }
        } catch (DAOException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro na conex√£o com o Banco de dados!");
            ex.printStackTrace();
        }
    }

