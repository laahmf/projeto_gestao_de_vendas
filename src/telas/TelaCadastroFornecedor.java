    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            String nome = txtRazaoSocial.getText();
            String telefone = txtTelefone.getText();
            String email = txtEmail.getText();
            String cnpj = txtCnpj.getText();
            if (!nome.equals("") && !telefone.equals("") && !email.equals("") && !cnpj.equals("")) {
                Fornecedor f = new Fornecedor(nome, telefone, email, cnpj);
                this.fornecedorDAO.incluir(f);
                JOptionPane.showMessageDialog(null, "Fornecedor Cadastrado com sucesso!");
                btnLimparActionPerformed(evt);
            } else { 
                JOptionPane.showMessageDialog(null, "Preencha todos os dados!");
            }
        } catch (DAOException | SQLException f) {
            JOptionPane.showMessageDialog(null, "erro ao cadastrar fornecedor no banco de dados!");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

