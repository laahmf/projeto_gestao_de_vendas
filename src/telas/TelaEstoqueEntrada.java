    private void limpar() {
        txtIdProdutoEntrada.setText("");
        txtProdutoSelecionado.setText("");
        txtDataEntrada.setValue(null);
        txtQuantidadeEstoque.setText("");
        produtoSelecionado = null;
    }
    
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String produto = txtIdProdutoEntrada.getText();
        String quantidade = txtQuantidadeEstoque.getText();
        String dataSaida = txtDataEntrada.getText();
        if (produtoSelecionado != null && !quantidade.equals("") && !dataSaida.equals("")) {
            try {
                EstoqueEntrada e = new EstoqueEntrada(produtoSelecionado, Integer.parseInt(quantidade), dataSaida);
                this.estoqueEntradaDAO.incluir(e);
                JOptionPane.showMessageDialog(null, "Entrada de estoque cadastrada com sucesso!");
                limpar();
            } catch (DAOException | SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao salvar entrada de estoque!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os dados!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            int idProduto = Integer.parseInt(txtIdProdutoEntrada.getText());
            Produto p = produtoDAO.buscarPorId(idProduto);
            if (p != null) {
                txtProdutoSelecionado.setText(p.toString());
                produtoSelecionado = p;
            } else {
                JOptionPane.showMessageDialog(null, "Produto n√£o encontrado!");
                txtProdutoSelecionado.setText("");
                produtoSelecionado = null;
            }
        } catch (DAOException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar produto!");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

